    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel modelo = (DefaultTableModel) jTCompras.getModel();
        modelo.setNumRows(0);
        DefaultTableModel modelo1 = (DefaultTableModel) jTVendas.getModel();
        modelo1.setNumRows(0);
        CompraSQL csql = new CompraSQL();
      
        lucro = 0;
        totalCompras = 0;
        totalVendas = 0;
      
        VendaSQL vsql = new VendaSQL();
        
        try {
            for(Compra c : csql.selectAllCompraDia()){
                modelo.addRow(new Object[]{
                    c.getCod_Compra(),
                    c.getData(),
                   df.format(c.getValorTotal())
                });
            }
        } catch (ParseException ex) {
            Logger.getLogger(TelaRelatório.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            for(Venda v : vsql.selectAllVendaDia()){
                modelo1.addRow(new Object[]{
                    v.getCod_Venda(),
                    v.getData(),
                    df.format(v.getValorTotal())
                });
            }
        } catch (ParseException ex) {
            Logger.getLogger(TelaRelatório.class.getName()).log(Level.SEVERE, null, ex);
        }
        
      for(int i = 0; i<jTCompras.getRowCount();i++){
                    
            try {
                String valor = (String)jTCompras.getValueAt(i,2);
                NumberFormat format = NumberFormat.getInstance(Locale.US);
                Number number = format.parse(valor);
                double d = number.doubleValue();  
                totalCompras += (d);
            } catch (ParseException ex) {
                Logger.getLogger(TelaRelatório.class.getName()).log(Level.SEVERE, null, ex);
            }
                 
      }
      
      for(int i = 0; i<jTVendas.getRowCount();i++){
            try {
                String valor = (String)jTVendas.getValueAt(i,2);
                NumberFormat format = NumberFormat.getInstance(Locale.US);
                Number number = format.parse(valor);
                double d = number.doubleValue();  
                totalVendas += (d);
                
         
            } catch (ParseException ex) {
                Logger.getLogger(TelaRelatório.class.getName()).log(Level.SEVERE, null, ex);
            }
                 
      }
      
      valorTC.setText("R$ "+df.format(totalCompras));
      ValorTV.setText("R$ "+df.format(totalVendas));
      
     
      lucro = totalVendas - totalCompras;
      
      if(lucro > 0){
         ValorL.setForeground(Color.blue);
      }else{
          ValorL.setForeground(Color.red);
      }
      ValorL.setText("R$ "+df.format(lucro));
        
        
    }//GEN-LAST:event_jRadioButton1ActionPerformed

