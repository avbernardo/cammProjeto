    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jRadioButton1.isSelected() || jRadioButton2.isSelected() || jRadioButton3.isSelected()){
            int reply = JOptionPane.showConfirmDialog(null, "Tem certeza?", "Confirmação", JOptionPane.YES_NO_OPTION);
             if (reply == JOptionPane.YES_OPTION)
     {
        Relatório f = new Relatório();
        RelatórioSQL fsql = new RelatórioSQL();
        
        java.util.Date dt = new java.util.Date();
        java.text.SimpleDateFormat sdf =
        new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
         sdf.setTimeZone(TimeZone.getTimeZone("GMT-3"));
        String currentTime = sdf.format(dt);
        
        f.setDataRelatório(currentTime);
        f.setLucro(lucro);
        if(jRadioButton1.isSelected()){
            f.setTipoRelatório("Diário");
        }else if(jRadioButton3.isSelected()){
            f.setTipoRelatório("Semanal");
        }else{
            f.setTipoRelatório("Mensal");
        }
       
        f.setTotalCompras(totalCompras);
        f.setTotalVendas(totalVendas);
        
        fsql.insertRelatório(f);
        ManterCompra c = new ManterCompra();
        c.setVisible(true);
        this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um tipo de fechamento");
        }
        }
        else{
             JOptionPane.getRootFrame().dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

