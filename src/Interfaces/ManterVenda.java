    public void readComboBoxes(){
        
        ClienteSQL fsql = new ClienteSQL();
        
        for(Cliente f: fsql.selectAllCliente()){
            comboF.addItem(f);
        }
        
         MaterialSQL msql = new MaterialSQL();
        
        for(Material m: msql.selectAllMaterial()){
            comboM.addItem(m);
        }
    }

    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTClientees.getModel();
        modelo.setNumRows(0);
        ItemVendaSQL csql = new ItemVendaSQL();
        

        for (ItemVenda ic : csql.selectAllItemVenda()) {

            modelo.addRow(new Object[]{
                ic.getCod_ItemVenda(),
                ic.getMaterial().getNome(),
                ic.getMaterial().getPreço_Kilo_Unidade(),
                ic.getPeso_Quantidade(),
                ic.getValorTotal(),
         
            });

        }

    }
    public void readJTableForDesc(String nome) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTClientees.getModel();
        modelo.setNumRows(0);
        ClienteSQL csql = new ClienteSQL();

        for (Cliente f : csql.selectForCliente(nome)) {
           
            
            modelo.addRow(new Object[]{
                f.getID_Cliente(),
                f.getNome(),
                f.getContato(),
                f.getBairro(),
                f.getLogradouro(),
                f.getNúmero()
            });
            
            

        }

    }

