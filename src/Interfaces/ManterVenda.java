    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) jTClientees.getModel();
        if(jTClientees.getSelectedRow() != -1){
            try {
                String valor = ((String) jTClientees.getValueAt(jTClientees.getSelectedRow(),3));
                NumberFormat format = NumberFormat.getInstance(Locale.US);
                Number number = format.parse(valor);
                double d = number.doubleValue();
                valorTotalCompra -= d;
                modelo.removeRow(jTClientees.getSelectedRow());
                jTClientees.setModel(modelo);
                
                valorTo.setText("R$ "+df.format(valorTotalCompra));
                if(valorTotalCompra == 0){
                    valorTo.setText("R$ 0.00");
                }
            } catch (ParseException ex) {
                Logger.getLogger(ManterVenda.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha para excluir");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

