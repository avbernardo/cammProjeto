    public void readComboBoxes(){
        
        FornecedorSQL fsql = new FornecedorSQL();
        
        for(Fornecedor f: fsql.selectAllFornecedor()){
            comboF.addItem(f);
        }
        
         MaterialSQL msql = new MaterialSQL();
        
        for(Material m: msql.selectAllMaterialNome()){
            comboM.addItem(m);
        }
    }

    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTFornecedores.getModel();
        modelo.setNumRows(0);
        ItemCompraSQL csql = new ItemCompraSQL();
        

        for (ItemCompra ic : csql.selectAllItemCompra()) {

            modelo.addRow(new Object[]{
                ic.getCod_ItemCompra(),
                ic.getMaterial().getNome(),
                ic.getMaterial().getPreço_Kilo_Unidade(),
                ic.getPeso_Quantidade(),
                ic.getValorTotal(),
         
            });

        }

    }
    public void readJTableForDesc(String nome) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTFornecedores.getModel();
        modelo.setNumRows(0);
        FornecedorSQL csql = new FornecedorSQL();

        for (Fornecedor f : csql.selectForFornecedor(nome)) {
           
            
            modelo.addRow(new Object[]{
                f.getID_Fornecedor(),
                f.getNome(),
                f.getContato(),
                f.getBairro(),
                f.getLogradouro(),
                f.getNúmero()
            });
            
            

        }

    }

