    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) jTFornecedores.getModel();
        if(jTFornecedores.getSelectedRow() != -1){
            try {
                String valor = ((String) jTFornecedores.getValueAt(jTFornecedores.getSelectedRow(),3));
                NumberFormat format = NumberFormat.getInstance(Locale.US);
                Number number = format.parse(valor);
                double d = number.doubleValue();
                
                valorTotalCompra -= d;
                modelo.removeRow(jTFornecedores.getSelectedRow());
                jTFornecedores.setModel(modelo);
                
                
                valorTo.setText("R$ "+df.format(valorTotalCompra));
                if(valorTotalCompra == 0){
                    valorTo.setText("R$ 0.00");
                } } catch (ParseException ex) {    
                Logger.getLogger(ManterCompra.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha para excluir");
        }
       
                    

    }//GEN-LAST:event_jButton3ActionPerformed

